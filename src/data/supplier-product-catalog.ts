function seedToProduct(seed: ProductSeed): SupplierRawProduct {
  const vendorImages = getVendorListingImageUrls({
    productId: seed.id,
    category: seed.category,
    supplierPhotoIds: seed.photos,
    imageCount: 4,
  });

  return {
    id: seed.id,
    platform: seed.platform,
    title: seed.title,
    description: seed.description,
    product_url: platformProductUrl(seed.platform, seed.id),
    images: vendorImages,
    cost_usd: seed.cost,
    currency: "USD",
    category: seed.category,
    trending_score: seed.score,
    shipping: seed.shipping,
    supplier_name: seed.supplierName,
    last_updated: LAST_UPDATED,
  };
}

